// check Mobile No
//////////////////////////////////////////////////
function checkMobileNo(val) {

    var correct = true;

    if (!check_digit(val)) {
        correct = false;
    }

    if (val.length != 10) {
        correct = false;
    }

    if (val.substring(0, 1) !== '0') {
        correct = false;
    }

    return correct;

}


