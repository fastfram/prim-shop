window.ajaxPost = function (url, data, callback) {
    dataSend = {
        'url': url,
        'method': "POST",
        'async': true,
        'crossDomain': true,
        'contentType': "application/json",
        'headers': {
            'content-type': "application/json"
        },
        'data': JSON.stringify(data)
    };

    $.ajax(dataSend)
        .done(function (response) {
            callback(response);

        })
        .fail(function (response) {
            console.error(response);

        });

}
