function check_digit(val) {
    if (val.trim() == '') {
        return false;
    } else {

        var numbers = /^[-+]?[0-9]+\.[0-9]+$/;
        var int_expr = /^-?[0-9]+$/;
        if (val.match(numbers) || val.match(int_expr)) {
            return true;
        } else {
            return false;
        }
    }
}

